const langSelector = document.getElementById('langs');

// Listen for changes on the dropdown menu
langSelector.addEventListener('change', function(event) {
    const lang = event.target.value;

    // Update Hero
    document.getElementById('hero-title').textContent = translations[lang].heroTitle;
    document.getElementById('hero-subtitle').textContent = translations[lang].heroSubtitle;
    document.getElementById('hero-desc').textContent = translations[lang].heroDesc;

    // Update Mission
    document.getElementById('mission-title').textContent = translations[lang].missionTitle;
    document.getElementById('mission-desc').textContent = translations[lang].missionDesc;

    // Update Cards
    document.getElementById('card1-title').textContent = translations[lang].c1Title;
    document.getElementById('card1-desc').textContent = translations[lang].c1Desc;
   
    document.getElementById('card2-title').textContent = translations[lang].c2Title;
    document.getElementById('card2-desc').textContent = translations[lang].c2Desc;
   
    document.getElementById('card3-title').textContent = translations[lang].c3Title;
    document.getElementById('card3-desc').textContent = translations[lang].c3Desc;
   
    document.getElementById('card4-title').textContent = translations[lang].c4Title;
    document.getElementById('card4-desc').textContent = translations[lang].c4Desc;
   
    document.getElementById('card5-title').textContent = translations[lang].c5Title;
    document.getElementById('card5-desc').textContent = translations[lang].c5Desc;

    // Apply RTL (Right-to-Left) formatting for Arabic
    if (lang === 'ar') {
        document.body.setAttribute('dir', 'rtl');
        // Optional: you can also change the font family for Arabic here if you want
    } else {
        document.body.setAttribute('dir', 'ltr');
    }
   
    // Update the HTML lang attribute for accessibility (Screen Readers)
    document.documentElement.lang = lang;
});
// --- Auto-Detect Browser Language on Page Load ---

// 1. Get the user's browser language (e.g., "es-ES", "en-US")
const userBrowserLang = navigator.language || navigator.userLanguage;

// 2. Extract just the first two letters (e.g., "es", "en")
const shortLangCode = userBrowserLang.substring(0, 2);

// 3. Check if we support their language, otherwise default to English
let defaultLang = 'en'; // Start with English as the fallback
if (translations[shortLangCode]) {
    defaultLang = shortLangCode;
}

// 4. Set the dropdown menu to match the detected language
langSelector.value = defaultLang;

// 5. Trigger the translation immediately by dispatching a 'change' event
langSelector.dispatchEvent(new Event('change'));
